/* More Declaration Examples, using various types*/
#include <iostream>

using namespace std;

int main()
{
   // declaration examples

   int x;
   int y(10);
   float average;
   char letter;

   x = 9;

   cout << "x = " << x << endl;
   cout << "y = " << y << endl;
   cout << "average = " << average << endl;
   cout << "letter = " << letter << endl;



}